def build_process():
    body = """
<section><div class="card">
<h2>Why a competition</h2>
<p>On real data you never learn whether your MMM was right — there is no answer key. So we built a
synthetic world where there <i>is</i> one. Every channel's true carryover, saturation, and effect are
generated from known parameters and <b>sealed</b> in <code>data_sealed/ground_truth.json</code>. A
hard rule, enforced by a test, keeps the modeling pipeline from ever reading it; only the grader may.
Every engine then competes to recover the truth it cannot see.</p>
</div></section>

<section><div class="card">
<h2>The synthetic world (the data-generating process)</h2>
<ul>
<li><b>Channels with distinct physics.</b> Six media channels, each with its own carryover (geometric
adstock θ from 0.10 to 0.75) and diminishing returns (Hill saturation), spanning saturated
(paid_social, paid_search, tv) and headroom (influencer, dooh) channels.</li>
<li><b>The confound.</b> Spend is paced to demand, so total spend correlates ~0.6 with seasonality —
the central reason naive attribution is biased.</li>
<li><b>Controls + noise.</b> Promotions, price, competitor pressure, holidays, plus realistic noise.</li>
<li><b>Experiments.</b> A rotating per-channel geo lift test, and a multi-cell <b>spend ladder</b>
that brackets each channel's operating point to measure (not assume) its response curve.</li>
<li><b>A hardened geo panel.</b> A 50-geo world with a latent, unobserved demand confounder (affluent
DMAs spend more <i>and</i> convert more) — to test whether geo-level models survive a real confound.</li>
</ul>
</div></section>

<section><div class="card">
<h2>The competitors</h2>
<ul>
<li><b>Naive OLS</b> — linear regression on spend. The blunt baseline.</li>
<li><b>Frequentist NLS</b> — non-linear least squares with adstock+Hill (now ridge-regularised).</li>
<li><b>DraftZone PyMC</b> — our regularised Bayesian MMM, observational and (optionally)
experiment-anchored.</li>
<li><b>Google Meridian</b> — the real package, with Fourier vs its native AKS seasonality, and on the
multi-geo panel with a spectrum of demand-proxy controls.</li>
<li><b>Meta Robyn</b> — the real R package (ridge + Nevergrad + Prophet + DECOMP.RSSD), plain and
experiment-calibrated, plus a faithful Python reimplementation.</li>
<li><b>Spend ladder</b> — a pure-experiment readout that fits the response curve from bracketed test
cells.</li>
</ul>
</div></section>

<section><div class="card">
<h2>How we score</h2>
<ul>
<li><b>MAE per channel</b> — mean absolute error of recovered avg weekly contribution vs sealed truth.
The headline metric.</li>
<li><b>Media bias</b> — total recovered media vs truth (over/under-crediting).</li>
<li><b>Interval coverage</b> — for Bayesian engines, does the 89% credible interval contain the truth?</li>
<li><b>Sampling quality (ESS / R-hat)</b> — a recovery you couldn't sample isn't a recovery.</li>
<li><b>Divergence guard</b> — an engine whose fit blew up is flagged and kept out of the chart.</li>
</ul>
<p>Geo engines are graded against the geo world's <i>own</i> answer key (it differs from national by
a small aggregation gap plus the confounder), never the national one.</p>
</div></section>

<section><div class="card">
<h2>The robustness protocol — why one dataset is never enough</h2>
<p>A single graded dataset answers "who won <i>this</i> world?"; that ranking is unstable. So we
regenerate the entire world under many seeds and aggregate by <b>average rank, win-rate, and MAE
spread</b>. Each seed is independent — new data, new experiments, new sealed truth — so nothing is
reused; every engine is recomputed every run. The honest output is a distribution with overlap, not
a podium. Tellingly, even the <i>robustness</i> ranking shifted between 6 and 10 seeds: at 6 seeds one
engine looked like a clear champion; at 10 its lead was inside the noise. Stability claims need
samples too.</p>
<p class="small">See the <a href="../robustness/index.html">multi-seed leaderboard</a> for the live
ranking, the <a href="../engines/index.html">single-seed deep dive</a> for one world in detail, and the
<a href="../results/index.html">results &amp; recommendations</a> for what it all means.</p>
</div></section>
"""
