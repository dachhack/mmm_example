    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(_page("How the MMM competition works",
                         "Sealed-truth grading, a dozen engines, and a multi-seed protocol.", body),
                   encoding="utf-8")
    print(f"Wrote {out}")


