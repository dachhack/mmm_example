def _leaderboard_table():
    runs = rb.load_runs()
    eng = rb.aggregate(runs)
    nat = {k: v for k, v in eng.items() if v["world"] == "national"}
    rows = sorted((rb._row(k, v, len(runs)) for k, v in nat.items()), key=lambda r: r["avg_rank"])
    out = []
    for r in rows:
        rank = f"{r['avg_rank']:.1f}" if np.isfinite(r["avg_rank"]) else "—"
        out.append([r["label"], str(r["n"]), rank, str(r["wins"]),
                    f"{r['med_mae']:.0f}", f"±{r['mae_std']:.0f}", f"{r['mean_bias']:+.0f}%",
                    f"{r['diverged']}" if r["diverged"] else "—"])
    tbl = mr._tbl(["engine", "runs", "avg rank ↓", "wins", "median MAE", "spread", "mean bias",
                   "diverged"], out)
    return tbl, len(runs)


